package com.ticketbooking.pojo;

public class searchListDC {
	private String flightNumber;
	private String airlineName;
	private String departureArrival;
	private String duration;
	private String numberOfStops;
	private String price;
	private String totalNumberOfResult;
	
	public String getFlightNumber() {
		return flightNumber;
	}
	public void setFlightNumber(String flightNumber) {
		this.flightNumber = flightNumber;
	}
	public String getAirlineName() {
		return airlineName;
	}
	public void setAirlineName(String airlineName) {
		this.airlineName = airlineName;
	}
	public String getDepartureArrival() {
		return departureArrival;
	}
	public void setDepartureArrival(String departureArrival) {
		this.departureArrival = departureArrival;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getNumberOfStops() {
		return numberOfStops;
	}
	public void setNumberOfStops(String numberOfStops) {
		this.numberOfStops = numberOfStops;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getTotalNumberOfResult() {
		return totalNumberOfResult;
	}
	public void setTotalNumberOfResult(String totalNumberOfResult) {
		this.totalNumberOfResult = totalNumberOfResult;
	}
	
}
